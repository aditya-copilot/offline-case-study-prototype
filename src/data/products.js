export const extractPriceConstraint = (query) => {
  const q = query.toLowerCase();
  
  // Match price patterns like: under 50k, below 50000, 30k to 50k, less than 25000
  const pricePatterns = [
    /under\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /below\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /less\s+than\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /within\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /upto\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /up\s+to\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /(\d+)\s*(?:k|000)\s*(?:rs|rupees|inr|₹)/i,
    /(?:rs|rupees|inr|₹)?\s*(\d+)(?:\s*(?:k|000))?/i
  ];
  
  for (const pattern of pricePatterns) {
    const match = q.match(pattern);
    if (match) {
      let amount = parseInt(match[1]);
      // If amount is less than 100, assume it's in thousands (e.g., "50k")
      if (amount < 100) {
        amount = amount * 1000;
      }
      return { maxPrice: amount, type: 'under' };
    }
  }
  
  return null;
};

export const extractCategoryAndBrand = (query) => {
  const q = query.toLowerCase().trim();
  
  const categoryKeywords = {
    'Motorcycle': ['motorcycle', 'bike', 'bike', 'motorcycle'],
    'Scooter': ['scooter', 'scooty'],
    'Electric Scooter': ['electric scooter', 'e-scooter', 'electric bike', 'ev']
  };
  
  const brandKeywords = {
    'Hero': ['hero', 'hero motocorp', 'hero motorcorp']
  };
  
  let detectedCategory = null;
  let detectedBrand = null;
  
  // Check for categories
  for (const [category, keywords] of Object.entries(categoryKeywords)) {
    if (keywords.some(kw => q.includes(kw))) {
      detectedCategory = category;
      break;
    }
  }
  
  // Check for brands
  for (const [brand, keywords] of Object.entries(brandKeywords)) {
    if (keywords.some(kw => q.includes(kw))) {
      detectedBrand = brand;
      break;
    }
  }
  
  return { category: detectedCategory, brand: detectedBrand };
};

export const searchProducts = (query) => {
  const q = query.toLowerCase().trim();
  
  // Extract price constraint
  const priceConstraint = extractPriceConstraint(q);
  
  // Extract category and brand
  const { category, brand } = extractCategoryAndBrand(q);
  
  return products.filter(p => {
    // Check price constraint
    if (priceConstraint && priceConstraint.type === 'under') {
      if (p.price > priceConstraint.maxPrice) {
        return false;
      }
    }
    
    // Check category match
    if (category) {
      if (p.category.toLowerCase() !== category.toLowerCase()) {
        return false;
      }
    }
    
    // Check brand match
    if (brand) {
      if (p.brand.toLowerCase() !== brand.toLowerCase()) {
        return false;
      }
    }
    
    // If no specific filters, do word-based search
    if (!category && !brand && !priceConstraint) {
      const queryWords = q.split(/\s+/).filter(w => w.length > 2);
      const productText = `${p.name} ${p.brand} ${p.category}`.toLowerCase();
      
      return queryWords.some(qw => 
        productText.includes(qw) || 
        p.name.toLowerCase().includes(qw) ||
        p.brand.toLowerCase().includes(qw)
      );
    }
    
    return true;
  });
};

export const getSearchMeta = (query) => {
  const priceConstraint = extractPriceConstraint(query);
  const { category, brand } = extractCategoryAndBrand(query);
  
  return {
    priceConstraint,
    category,
    brand,
    query: query.trim()
  };
};

