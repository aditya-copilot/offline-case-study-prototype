export const extractPriceConstraint = (query) => {
  const q = query.toLowerCase()
  
  // Match price patterns like: under 50k, below 50000, 30k to 50k, less than 25000
  const pricePatterns = [
    /under\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /below\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /less\s+than\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /within\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /upto\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /up\s+to\s+(\d+)[kK]?(?:\s*(?:rs|rupees|inr|₹))?/i,
    /(\d+)\s*(?:k|000)\s*(?:rs|rupees|inr|₹)/i,
    /(?:rs|rupees|inr|₹)?\s*(\d+)(?:\s*(?:k|000))?/i
  ]
  
  for (const pattern of pricePatterns) {
    const match = q.match(pattern)
    if (match) {
      let amount = parseInt(match[1])
      // If amount is less than 100, assume it's in thousands (e.g., "50k")
      if (amount < 100) {
        amount = amount * 1000
      }
      return { maxPrice: amount, type: 'under' }
    }
  }
  
  return null
}

